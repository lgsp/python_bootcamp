letter_z = "z"
print("Check letter with .isalpha()")
print("Is z a letter:", letter_z.isalpha())
print("Check letter or number with .isalnum()")
print("Is z a letter or number:", letter_z.isalnum())
print("Check number with .isdigit()")
print("Is z a number:", letter_z.isdigit())
print("Check lowercase with .islower()")
print("Is z a lowercase:", letter_z.islower())
print("Check uppercase with .isupper()")
print("Is z a uppercase:", letter_z.isupper())
print("Check space with .isspace()")
print("Is z is a space:", letter_z.isspace())

